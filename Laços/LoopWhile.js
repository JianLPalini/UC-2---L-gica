// While - Enquanto

// Equanto uma condição for verdadeira, ele executa o meu código

let voltas = 0;
while (voltas <= 10){
    console.log(`Você já deu ${voltas} voltas.`)
    //voltas = voltas +1
    //voltas += 1
    voltas ++
}
console.log("Parabens! Você completou a corrida!")

